    //disabling generateBillPrint button if the conditions are not met
    public void validateField(){
        String customerName = txtCusName.getText();
        String customerMobileNo = txtCusMobileNo.getText();
        String customerEmail = txtCusEmail.getText();
        if(!customerEmail.equals("") && customerMobileNo.matches(mobileNumberPattern) && customerMobileNo.length() == 10 && customerEmail.matches(emailPattern) && grandTotal > 0){
            btnGenerateBillPrint.setEnabled(true);
        }
        else{
            btnGenerateBillPrint.setEnabled(false);
        }
    }
